.generateSet5 <- function(
  prob.list,
  prob.matrix,
  num,
  s.cells,
  n.cell.types,
  index.ex
) {
  n <- ceiling(num * s.cells/1000)
  if (!is.null(index.ex)) {
    generator <- function() {
      gtools::rdirichlet(
        n = 1,
        alpha = .cellExcluder(rep(1, n.cell.types), index.ex = index.ex)[[1]]
      )
    }
    probs <- t(replicate(n = n, expr = generator(), simplify = TRUE))
  } else {
    probs <- gtools::rdirichlet(n, rep(1, n.cell.types))
  }
  probs <- round(probs * 100)
  probs <- t(apply(X = probs, MARGIN = 1,
                   FUN = function(x) .setHundredLimit(
                     x = x, index.ex = index.ex
                   )))
  colnames(probs) <- colnames(prob.matrix)
  return(probs)
}


.generateSet6 <- function(
  prob.list,
  prob.matrix,
  num,
  s.cells,
  n.cell.types,
  index.ex
) {
  probs <- list()
  n <- ceiling(num * s.cells/1000)
  while (length(probs) < n) {
    probs[[length(probs) + 1]] <- unlist(lapply(X = prob.list, FUN = sample, 1))
  }
  probs <- lapply(X = probs, FUN = function(x) return(round(x * 100 / sum(x))))
  probs <- lapply(X = probs, FUN = sample)
  probs <- lapply(X = probs, FUN = function(x) x[names(prob.list)])
  probs <- matrix(unlist(probs), nrow = n, byrow = T)
  probs <- t(apply(X = probs, 1, FUN = function(x) {
    .adjustHundred(x = x,
                   prob.list = prob.list,
                   index.ex = index.ex
    )
  }))
  colnames(probs) <- colnames(prob.matrix)
  return(probs)
}


#' Generate training and test simulated bulk samples.
#'
#' Generate training and test bulk samples using the probability matrices
#' built by \code{generateTrainAndTestBulkProbMatrix} function.
#' These samples are generated using the assumption that the expression of gene \eqn{i}
#' from sample \eqn{j} is
#' given by the sum of the cell type specific expression \eqn{X_{ijk}}
#' weighted by the proportions of cell type \eqn{k} in the sample determined by
#' the probability matrix.
#'
#' As described in Description section, bulk samples are built by the sum of
#' the gene expression of a determined cell type weighted by the proportions
#' of cell type determined by the probability matrix following the following equation:
#'
#' \deqn{e_{gk} = \Sum^{w_i}_{i = 1} e_{gi} 1}
#'
#' The number of bulk samples is determined by dimensions of probability matrix.
#' See \code{\link{generateTrainAndTestBulkProbMatrix}} for details.
#'
#' \code{digitalDLSorteR} allows the use of HDF5 files as backend for the resulting data
#' using \code{DelayedArray} and \code{HDF5Array} frameworks in cases of generating
#' too large expression matrix to work with it loaded in memory. You need to provide
#' a valid file path to store the resulting file with '.h5' extension. The data
#' will be accessible from R without being loaded into memory. This option
#' slightly slows down execution times, since subsequent transformations of data
#' will be carried out by chunks instead of using all data. We recommend this
#' options due to the large size of the simulated matrices.
#'
#' @param object \code{DigitalDLSorter} object with \code{single.cell.sim} and
#' \code{prob.cell.types} slots.
#' @param type.data Type of data to generate among 'train', 'test' or 'both'
#' (the last by default).
#' @param file.backend Valid file path where to save the HDF5 file used as backend.
#' If it is equal to \code{NULL} (by default), the data are produced and loaded in memory.
#' @param threads Number of threads used during the generation of bulk samples
#' (2 by default).
#' @param verbose Show messages during the execution.
#' @return A DigitalDLSorter object with \code{bulk.sim} slot containing
#' a list with one or two entries (depending on selected \code{type.data} argument):
#' 'train' and 'test'. Each entry contains a \code{SummarizedExperiment} object
#' with simulated bulk samples in \code{assay} slot, sample names in
#' \code{colData} slot and feature names in \code{rowData} slot.
#'
#' @export
#'
#' @seealso \code{\link{generateTrainAndTestBulkProbMatrix}},
#' \code{\link{ProbMatrixCellType}}.
#'
#' @examples
#' ## loading all data in memory
#' DDLSChung <- generateBulkSamples(
#'   DDLSChung,
#'   threads = 2,
#'   type.data = "both"
#' )
#'
#' ## using HDF5 as backend
#' DDLSChung <- generateBulkSamples(
#'   DDLSChung,
#'   threads = 2,
#'   type.data = "both",
#'   file.backend = "DDLSChung.bulk.sim.h5"
#' )
#'
#' @references
#' Pagès H, Hickey wcfP, Lun A (2020). DelayedArray: A unified framework for
#' working transparently with on-disk and in-memory array-like datasets.
#' R package version 0.14.1.
#'
#' Pagès H (2020). HDF5Array: HDF5 backend for DelayedArray objects. R package
#' version 1.16.1.
#'
generateBulkSamples <- function(
  object,
  type.data = "both",
  file.backend = NULL,
  threads = 2,
  verbose = TRUE
) {
  if (class(object) != "DigitalDLSorter") {
    stop("The object provided is not of DigitalDLSorter class")
  } else if (is.null(single.cell.sim(object))) {
    stop("single.cell.sim slot is empty")
  } else if (is.null(prob.cell.types(object))) {
    stop("prob.cell.types slot is empty")
  } else if (!any(type.data == c("train", "test", "both"))) {
    stop("type.data argument must be one of the next options: train, test or both")
  }
  # else if (object@prob.cell.types$train@cell.names) {
  #   stop("prob.cell.types is not a valid object")
  # }
  if (!is.null(file.backend)) {
    if (file.exists(file.backend)) {
      stop("file.backend already exists. Please provide a correct file path")
    }
  }
  if (threads <= 0) {
    threads <- 1
  }
  if (verbose) {
    message(paste("=== Set parallel environment to", threads, "threads"))
  }
  sim.counts <- assay(object@single.cell.sim)
  sim.counts <- edgeR::cpm.default(sim.counts) # sure???

  if (type.data == "both") {
    if (!is.null(object@bulk.sim)) {
      warning("'bulk.sim' slot will be overwritten",
              call. = FALSE, immediate. = TRUE)
    }
    bulk.counts <- lapply(
      X = c("train", "test"),
      FUN = function(x) {
        if (verbose) {
          message(paste("\n=== Generating", x, "bulk samples:"))
        }
        .generateBulkProfiles(
          object = object,
          sim.counts = sim.counts,
          type.data = x,
          file.backend = file.backend,
          threads = threads,
          verbose = verbose
        )
      }
    )
    names(bulk.counts) <- c("train", "test")
    object@bulk.sim <- bulk.counts
  } else {
    if (!is.null(object@bulk.sim) && type.data %in% names(object@bulk.sim)) {
      warning(paste(type.data, "data in 'bulk.sim' slot will be overwritten"),
              call. = FALSE, immediate. = TRUE)
    }
    if (verbose) {
      message(paste("\n=== Generating", type.data, "bulk samples:"))
    }
    bulk.counts <- .generateBulkProfiles(
      object = object,
      sim.counts = sim.counts,
      type.data = type.data,
      file.backend = file.backend,
      threads = threads,
      verbose = verbose
    )
    if (!is.null(object@bulk.counts)) {
      if (type.data %in% names(object@bulk.counts)) {
        object@bulk.sim[[type.data]] <- NULL
      }
      object@bulk.sim <- c(object@bulk.sim, type.data = bulk.counts)
    } else {
      object@bulk.sim[[type.data]] <- bulk.counts
    }
  }

  message("\nDONE")
  return(object)
}

setBulks <- function (x, c, i) {
  return(rowSums(c[, x]))
}

.generateBulkProfiles <- function(
  object,
  sim.counts,
  type.data,
  file.backend,
  threads,
  verbose
) {
  prob.matrix.names <- object@prob.cell.types[[type.data]]@cell.names
  bulk.counts <- pbapply::pbapply(
    X = prob.matrix.names,
    MARGIN = 1,
    FUN = setBulks,
    c = sim.counts,
    cl = threads
  )
  colnames.bulk <- paste("Bulk", seq(dim(bulk.counts)[2]), sep = "_")
  rownames.bulk <- rownames(bulk.counts)
  if (!is.null(file.backend)) {
    if (verbose) {
      message("\nWriting data on disk:\n")
    }
    bulk.counts <- DelayedArray::DelayedArray(bulk.counts)
    bulk.counts <- HDF5Array::writeHDF5Array(
      bulk.counts,
      filepath = file.backend,
      name = type.data,
      verbose = verbose
    )
    # en R 4.0 implementaron la posibilidad de guardar dimnames
    # de momento lo meto en un slot
  }
  return(SummarizedExperiment::SummarizedExperiment(
    assays = list(counts = bulk.counts),
    rowData = rownames.bulk,
    colData = colnames.bulk))
}

## prepareDataForTraining function ---------------------------------

#' Generate training and test final data for training DNN model.
#'
#' Generate training and test final data for training Deep Neural Network model.
#' To do this, first single-cell and bulk profiles are combined into a single
#' expression matrix. Then, counts per million (CPM) in log2-space are computed and
#' normalized. Finally, all samples are shuffled in order to avoid biases
#' during training. Note that expression matrix is transposed in order to prepare
#' data for training.
#'
#' \code{digitalDLSorteR} allows the use of HDF5 files as backend for the resulting data
#' using \code{DelayedArray} and \code{HDF5Array} frameworks in cases of generating
#' too large expression matrix to work with it loaded in memory. You need to provide
#' a valid file path to store the resulting file with '.h5' extension. The data
#' will be accessible from R without being loaded into memory. This option
#' slightly slows down execution times, since subsequent transformations of data
#' will be carried out by chunks instead of using all data. We
#' recommend this options due to the large size of the simulated matrices.
#'
#' @param object \code{DigitalDLSorter} object with \code{single.cell.sim} and
#' \code{prob.cell.types} slots.
#' @param type.data Type of data to generate among 'train', 'test' or 'both'
#' (the last by default).
#' @param file.backend Valid file path where to save the HDF5 file used as backend.
#' If it is equal to \code{NULL} (by default), the data are produced and loaded in memory.
#' @param verbose Show messages during the execution.
#' @return A DigitalDLSorter object with \code{final.data} slot containing
#' a list with one or two entries (depending on selected \code{type.data} argument):
#' 'train' and 'test'. Each entry contains a \code{SummarizedExperiment} object
#' with single-cell and bulk samples combined in \code{assay} slot, sample names in
#' \code{rowData} slot and feature names in \code{colData} slot.
#'
#' @export
#'
#' @seealso \code{\link{generateBulkSamples}},
#' \code{\link{generateTrainAndTestProbMatrix}}.
#'
#' @examples
#' ## loading all data in memory
#' DDLSChung <- prepareDataForTraining(
#'   object = DDLSChung,
#'   type.data = "both",
#'   verbose = TRUE
#' )
#'
#' ## using HDF5 as backend
#' DDLSChung <- prepareDataForTraining(
#'   object = DDLSChung,
#'   type.data = "both",
#'   file.backend = "DDLSChung.final.data.combined.h5",
#'   verbose = TRUE
#' )
#'
prepareDataForTraining <- function(
  object,
  type.data,
  combine = TRUE,
  file.backend = NULL,
  verbose = TRUE
) {
  if (class(object) != "DigitalDLSorter") {
    stop("The object provided is not of DigitalDLSorter class")
  } else if (is.null(single.cell.sim(object))) {
    stop("single.cell.sim slot is empty")
  } else if (is.null(prob.cell.types(object))) {
    stop("prob.cell.types slot is empty")
  } else if (is.null(bulk.sim(object))) {
    stop("bulk.sim slot is empty")
  } else if (!any(type.data == c("train", "test", "both"))) {
    stop("type.data argument must be one of the next options: train, test or both")
  } else if (!any(names(bulk.sim(object)) %in% c("train", "test"))) {
    stop("bulk.sim slot is not correctly built")
  }
  if (type.data == "both") {
    if (!all(names(bulk.sim(object)) %in% c("train", "test")))
      stop("If type.data = 'both', bulk.sim slot must contain train and test data")
  } else {
    if (names(bulk.sim(object)) != type.data)
      stop(paste(type.data, "data is not present in bulk.sim slot"))
  }
  if (!is.null(object@final.data)) {
    warning("'final.data' slot will be overwritten",
            call. = FALSE, immediate. = TRUE)
  }
  if (is.null(file.backend)) {
    if (verbose) {
      message("=== Working in memory")
    }
    combineBulkSCProfiles <- .combineBulkSCProfilesInMemory
  } else {
    if (file.exists(file.backend)){
      stop("file.backend already exists. Please provide a correct file path")
    }
    if (verbose) {
      message("=== Working with HDF5 backend")
    }
    combineBulkSCProfiles <- .combineBulkSCProfilesHDF5
  }
  if (type.data == "both") {
    combined.counts <- lapply(
      X = c("train", "test"),
      FUN = function(x) {
        if (verbose) {
          message(paste("\n=== Preparing", x, "counts\n"))
        }
        combineBulkSCProfiles(
          object = object,
          type.data = x,
          combine = combine,
          file.backend = file.backend,
          verbose = verbose
        )
      }
    )
    names(combined.counts) <- c("train", "test")
    object@final.data <- combined.counts
  } else {
    if (verbose) {
      message(paste("\n=== Preparing", type.data, "counts for training\n"))
    }
    combined.counts <- combineBulkSCProfiles(
      object = object,
      type.data = type.data,
      combine = combine,
      file.backend = file.backend,
      verbose = verbose
    )
    if (!is.null(object@bulk.counts)) {
      if (type.data %in% names(object@bulk.counts)) {
        object@bulk.sim[[type.data]] <- NULL
      }
      object@final.data <- c(object@final.data, type.data = combined.counts)
    } else {
      object@final.data <- list(type.data = combined.counts)
    }
  }

  message("\nDONE")
  return(object)
}


