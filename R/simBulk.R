                           rownames(assay(object@single.cell.sim)))
    sample.names <- c(
      colnames(assay(object@single.cell.sim)[,
                                             unlist(object@prob.cell.types[[type.data]]@set.list)]),
      colData(object@bulk.sim[[type.data]])[[1]]
